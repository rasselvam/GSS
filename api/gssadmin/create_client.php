<?php
header('Content-Type: application/json');
session_start();

require_once __DIR__ . '/../../config/db.php';
require_once __DIR__ . '/upload_helpers.php';

header('Access-Control-Allow-Origin: *');
header('Access-Control-Allow-Methods: POST, OPTIONS');
header('Access-Control-Allow-Headers: Content-Type, Authorization, X-Requested-With');

if ($_SERVER['REQUEST_METHOD'] === 'OPTIONS') {
    http_response_code(200);
    exit;
}

function post_bool(string $key): int {
    return !empty($_POST[$key]) ? 1 : 0;
}

function post_str(string $key, string $default = ''): string {
    return trim((string)($_POST[$key] ?? $default));
}

function post_int(string $key, ?int $default = null): ?int {
    if (!isset($_POST[$key]) || $_POST[$key] === '') return $default;
    return (int)$_POST[$key];
}

function item_bool(array $item, string $key): int {
    return !empty($item[$key]) ? 1 : 0;
}

function item_str(array $item, string $key, string $default = ''): string {
    return trim((string)($item[$key] ?? $default));
}

function item_int(array $item, string $key, ?int $default = null): ?int {
    if (!array_key_exists($key, $item) || $item[$key] === '' || $item[$key] === null) return $default;
    return (int)$item[$key];
}

function friendlyDbError(PDOException $e): string {
    $sqlState = (string)$e->getCode();
    $info = $e->errorInfo ?? null;
    $driverCode = is_array($info) ? (int)($info[1] ?? 0) : 0;
    $driverMsg = is_array($info) ? (string)($info[2] ?? '') : '';

    if ($driverCode === 1305 || stripos($driverMsg, 'does not exist') !== false) {
        return 'Database procedure missing. Please import/run the required stored procedures (e.g. SP_Vati_Payfiller_CreateClient) in the active database.';
    }

    if ($driverCode === 1054 || stripos($driverMsg, 'Unknown column') !== false) {
        $missing = '';
        if ($driverMsg !== '' && preg_match("/Unknown column '([^']+)'/i", $driverMsg, $m)) {
            $missing = (string)($m[1] ?? '');
        }
        if ($missing !== '') {
            return "Database schema mismatch: missing column `$missing`. Please ALTER TABLE Vati_Payfiller_Clients to add it (or import the latest schema).";
        }
        return 'Database schema mismatch (unknown column). Please update/import the latest table schema for Vati_Payfiller_Clients.';
    }

    if ($driverCode === 1062 || stripos($driverMsg, 'Duplicate entry') !== false) {
        return 'Customer name already exists. Please use a different Customer Name.';
    }

    if ($sqlState === '23000') {
        return 'Database constraint error. Please check your inputs.';
    }

    return 'Database error. Please try again.';
}

function parse_locations(string $raw): array {
    $raw = str_replace(["\r\n", "\r"], "\n", $raw);
    $raw = str_replace([',', ';'], "\n", $raw);
    $parts = array_map('trim', explode("\n", $raw));
    $out = [];
    foreach ($parts as $p) {
        if ($p === '') continue;
        $out[$p] = true;
    }
    return array_keys($out);
}

try {
    if ($_SERVER['REQUEST_METHOD'] !== 'POST') {
        http_response_code(405);
        echo json_encode(['status' => 0, 'message' => 'Method not allowed']);
        exit;
    }

    $payload = file_get_contents('php://input');
    $decoded = null;
    if (is_string($payload) && trim($payload) !== '') {
        $decoded = json_decode($payload, true);
    }

    if (!empty($_FILES)) {
        $items = [$_POST];
    } elseif (is_array($decoded) && array_keys($decoded) === range(0, count($decoded) - 1)) {
        $items = $decoded;
    } elseif (is_array($decoded)) {
        $items = [$decoded];
    } else {
        $items = [$_POST];
    }

    if (!is_array($items) || count($items) === 0) {
        http_response_code(400);
        echo json_encode(['status' => 0, 'message' => 'Invalid payload. Expected JSON object/array or form-data.']);
        exit;
    }

    $pdo = getDB();

    $out = [];
    $hadError = false;

    foreach ($items as $idx => $item) {
        if (!is_array($item)) {
            $hadError = true;
            $out[] = ['input' => $item, 'status' => 0, 'error' => 'Invalid item format'];
            continue;
        }

        $customer_name = item_str($item, 'customer_name');
        $poc_name = '';
        $poc_email = '';
        $poc_phone = '';

        if ($customer_name === '') {
            $hadError = true;
            $out[] = ['input' => $item, 'status' => 0, 'error' => 'Customer name is required.'];
            continue;
        }

        $logoPath = item_str($item, 'customer_logo_path', '');
        if (!empty($_FILES) && $idx === 0 && !empty($_FILES['customer_logo']) && is_uploaded_file($_FILES['customer_logo']['tmp_name'] ?? '')) {
            $upload = upload_customer_logo($customer_name);
            if (!$upload['ok']) {
                $hadError = true;
                $out[] = ['input' => $item, 'status' => 0, 'error' => $upload['message'] ?? 'Logo upload failed.'];
                continue;
            }
            if (!empty($upload['path'])) {
                $logoPath = (string)$upload['path'];
            }
        }

        $sowPath = item_str($item, 'sow_pdf_path', '');
        if (!empty($_FILES) && $idx === 0) {
            $sowUpload = upload_sow_pdf($customer_name);
            if (!$sowUpload['ok']) {
                $hadError = true;
                $out[] = ['input' => $item, 'status' => 0, 'error' => $sowUpload['message'] ?? 'SOW upload failed.'];
                continue;
            }
            if (!empty($sowUpload['path'])) {
                $sowPath = (string)$sowUpload['path'];
            }
        }

        $params = [
            $customer_name,
            item_str($item, 'location', ''),
            item_str($item, 'short_description', ''),
            item_str($item, 'detailed_description', ''),
            item_bool($item, 'show_customer_logo'),
            $logoPath,

            item_bool($item, 'authorization_form_required'),
            item_str($item, 'authorization_type', 'none'),
            item_bool($item, 'submit_auth_before_dataentry'),
            item_bool($item, 'enable_delegation_after_auth'),
            item_bool($item, 'unique_reference_required'),
            item_bool($item, 'extra_fields_required'),
            0,

            item_bool($item, 'candidate_mail_required'),
            item_bool($item, 'reminder_mail_required'),
            item_bool($item, 'insuff_reminders_required'),
            item_bool($item, 'show_candidate_verification_status'),
            item_bool($item, 'contact_support_required'),
            item_bool($item, 'additional_fields_required'),
            item_bool($item, 'basic_details_masked_required'),

            item_str($item, 'delegation_mechanism', ''),
            item_str($item, 'save_button_instruction', ''),
            item_str($item, 'authorization_submit_instruction', ''),

            $poc_name,
            $poc_email,
            $poc_phone,
            '',
            '',

            item_int($item, 'internal_tat'),
            item_int($item, 'external_tat'),
            item_int($item, 'escalation_days'),
            item_str($item, 'weekend_rules', ''),

            item_str($item, 'auto_allocation', ''),
            item_str($item, 'candidate_notification', ''),
            '',
            $sowPath,
        ];

        try {
            $placeholders = rtrim(str_repeat('?,', count($params)), ',');
            $stmt = $pdo->prepare("CALL SP_Vati_Payfiller_CreateClient($placeholders)");

            try {
                $stmt->execute($params);
            } catch (PDOException $e) {
                $driverMsg = '';
                $info = $e->errorInfo ?? null;
                if (is_array($info)) {
                    $driverMsg = (string)($info[2] ?? '');
                }

                if (stripos($driverMsg, 'Incorrect number of arguments') !== false || stripos($e->getMessage(), 'Incorrect number of arguments') !== false) {
                    // Backward compatibility: older SP signature did not include location.
                    $legacyParams = $params;
                    array_splice($legacyParams, 1, 1);
                    $legacyPlaceholders = rtrim(str_repeat('?,', count($legacyParams)), ',');
                    $stmt = $pdo->prepare("CALL SP_Vati_Payfiller_CreateClient($legacyPlaceholders)");
                    $stmt->execute($legacyParams);
                } else {
                    throw $e;
                }
            }

            $row = $stmt->fetch(PDO::FETCH_ASSOC) ?: [];
            $clientId = isset($row['client_id']) ? (int)$row['client_id'] : 0;

            while ($stmt->nextRowset()) {
            }

            if ($clientId > 0) {
                try {
                    $rawLocations = item_str($item, 'location', '');
                    $locations = parse_locations($rawLocations);

                    $delStmt = $pdo->prepare('CALL SP_Vati_Payfiller_DeleteClientLocations(?)');
                    $delStmt->execute([$clientId]);
                    while ($delStmt->nextRowset()) {
                    }

                    if (!empty($locations)) {
                        $addStmt = $pdo->prepare('CALL SP_Vati_Payfiller_AddClientLocation(?, ?)');
                        foreach ($locations as $loc) {
                            $addStmt->execute([$clientId, $loc]);
                            while ($addStmt->nextRowset()) {
                            }
                        }
                    }
                } catch (Throwable $e) {
                }
            }

            $out[] = [
                'input' => $item,
                'status' => 1,
                'result' => ['client_id' => $clientId]
            ];
        } catch (PDOException $e) {
            $hadError = true;
            $out[] = ['input' => $item, 'status' => 0, 'error' => friendlyDbError($e)];
        }
    }

    echo json_encode([
        'status' => $hadError ? 0 : 1,
        'message' => $out
    ]);

} catch (PDOException $e) {
    http_response_code(500);
    echo json_encode(['status' => 0, 'message' => friendlyDbError($e)]);
} catch (Throwable $e) {
    http_response_code(400);
    echo json_encode(['status' => 0, 'message' => $e->getMessage()]);
}
